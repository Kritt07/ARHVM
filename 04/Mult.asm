// Программа должна умножать числа из R0 и R1 и сохранять результат в R2.

// умножение R0 на R1 можно реализовать при помощи цикла
// нужно складывать R2 должно прибольвять к себе число R0 R1 раз

// Инициализация
@R0
D = M
@value 
M = D

@R1
D = M
@n 
M = D

@R2
M = 0

@i
M = 0

// Цикл умножения
(LOOP)
    // Проверка условия: если i >= n, выходим
    @i
    D = M
    @n
    D = D - M
    @END
    D ; JGE

    // Тело цикла: R2 += value 
    @value
    D = M
    @R2
    M = D + M
    
    // i++
    @i
    M = M + 1
    
    @LOOP
    0 ; JMP

(END)
    @END
    0 ; JMP